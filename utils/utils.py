def zeroshot_classifier(classnames, templates, model):
    with torch.no_grad():
        zeroshot_weights = []
        for classname in tqdm(classnames):
            texts = [template.format(classname) for template in templates] #format with class
            texts = clip.tokenize(texts).cuda() #tokenize
            class_embeddings = model.encode_text(texts) #embed with text encoder
            class_embeddings /= class_embeddings.norm(dim=-1, keepdim=True)
            class_embedding = class_embeddings.mean(dim=0)
            class_embedding /= class_embedding.norm()
            zeroshot_weights.append(class_embedding)
        zeroshot_weights = torch.stack(zeroshot_weights, dim=1).cuda()
    return zeroshot_weights


def get_image_grid(images, is_path=True, max_images=24):
    transform = torchvision.transforms.Compose([
        torchvision.transforms.ToTensor()
    ])

    images = images[:max_images]

    if not is_path:
        image_grid = torchvision.utils.make_grid(images)
    else:
        image_grid = torchvision.utils.make_grid(torch.stack([transform(Image.open(image)).float() for image in images]))

    return image_grid


def gen_plot(img, title='visualization', size=100, one_channel=False, show=False):
    """Create a pyplot plot and save to buffer."""
    fig = plt.figure(figsize=(size, size))
    
    if one_channel:
        img = img.mean(dim=0)
    npimg = img.numpy()

    if one_channel:
        plt.imshow(npimg, cmap="Greys")
    else:
        plt.imshow(np.transpose(npimg, (1, 2, 0)))
    
    plt.title(title)
    plt.xticks([])
    plt.yticks([])
    plt.tight_layout()

    plt.savefig(os.path.join('results\plots', title+'.jpg'))
    if show:
        plt.show()
        


